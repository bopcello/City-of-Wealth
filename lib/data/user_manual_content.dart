## Welcome

You just got your first job or launched your first business idea. Money is hitting your account for the first time in your life, and nobody taught you what to do with it. That is exactly what this game is for.

City of Wealth is not a budgeting app where you log every coffee you buy. It is not a trading app where you guess stock prices. It is a simulation of real financial life — where the choices you make with your money, your time, and your habits have consequences that compound over time, just like they do in real life.

There are two things you are always managing: your **Gems** (your money) and your **KP** (your financial wisdom). Neither one alone makes you successful. You need both.

