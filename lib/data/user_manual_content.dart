## The Quiz System — Building Your KP

The primary way to earn KP is through quizzes. These are financial literacy questions that teach you real-world concepts while you play.

Each career level contains 20 quizzes: 3 Easy, 15 Medium, and 2 Hard.

| Difficulty | Correct Answer | Wrong Answer |
|:---:|:---:|:---:|
| Easy | +30 KP | -50 KP |
| Medium | +40 KP | -40 KP |
| Hard | +50 KP | -30 KP |

Notice something important here: Easy quizzes punish wrong answers more harshly than Hard quizzes do. This is intentional. Fundamentals matter most. Getting a basic concept wrong is more damaging than struggling with an advanced one.

If you replay a quiz you have already completed, the rewards are reduced to encourage you to move forward: +0 KP for Easy repeats, +10 KP for Medium, and +15 KP for Hard. You can still practise — you just earn less for going over old ground.

### The Daily Quiz

Every day, a fresh quiz is available. Attempting it — regardless of whether you get it right — keeps your streak alive. A correct answer gives you +20 KP. A wrong answer has no penalty. The daily quiz is your no-risk daily habit.

You can also attempt past daily quizzes for +10 KP each, though these do not count toward your streak.

