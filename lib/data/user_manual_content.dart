## The Streak System — The Power of Consistency

Your streak tracks how many consecutive days you have engaged with the game. It is maintained simply by attempting the daily quiz — you do not need to answer it correctly.

This reflects a real financial truth: consistency matters more than perfection. Someone who invests a little every month for 30 years beats someone who tries to perfectly time the market occasionally.

Streak rewards are cumulative discounts and income boosts:

| Streak | Reward |
|:---:|:---:|
| 10+ Days (Regular Customer) | 5% discount on purchasing assets |
| 30+ Days (Consistency Master) | 5% discount on assets + 5% boost to passive income |
| 100+ Days (Master of Money) | 10% discount on assets + 5% boost to passive income |

### Revivals — Your Safety Net

You automatically earn 1 Revival for every 10 days of continuous streak, up to a maximum of 5 stored at any time. If you miss a day, the game uses one Revival to protect your streak automatically. If you have no Revivals left, your streak resets to zero.

Think of Revivals as your emergency buffer — the same way a small cash reserve protects you from losing everything when an unexpected expense hits.

