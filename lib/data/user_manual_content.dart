## The Two Tracks

When you start, you choose a career path. Everything — your income, your buildings, your progression — flows from this choice.

**The Job Track** is for players who want a stable salary that grows as they climb the corporate ladder. You start small, earn consistently, and grow by proving yourself through assets and responsibilities.

**The Business Track** is for players who want to build something of their own. The income potential is higher, but the climb is steeper.

You cannot switch tracks mid-game, so think about which path reflects how you want to learn about financial growth.

