## KP — Your Financial Wisdom

KP stands for Knowledge Points. Think of it like a credit score, but broader. It measures not just whether you pay your bills, but whether you are making smart decisions for your life stage — where you live, what you eat, how you commute, how much you work, and how well you understand financial concepts.

KP and Gems do not convert into each other. You cannot spend KP to get Gems or vice versa. But they are deeply connected: making good decisions with your Gems raises your KP, and making bad ones lowers it. Just like in real life, your financial habits shape your financial reputation.

### Daily Living Choices

Every cycle, you make three everyday decisions. Each one affects your KP based on how appropriate it is for your current income and career level. The game rewards balance, not just frugality.

**Housing**

- Shared Studio: +5 to +10 KP. Great when you are starting out. But if you are a CEO trying to run a company from a shared room, your productivity suffers — expect a penalty.
- Small Apartment: +5 to +20 KP. The reliable middle ground for most of your career.
- Luxury House: High penalty, up to -150 KP, unless your income genuinely supports it. This reflects a real trap that many first-time earners fall into — upgrading their lifestyle before their income can sustain it.

**Food**

- Balanced Diet: +30 to +60 KP. Eating well is an investment in your productivity and health.
- Cheap Food: -10 to -100 KP. Saving money on food at the cost of your health is a short-term gain with long-term consequences.
- Buffet: -20 to -80 KP. Expensive and unhealthy. However, the penalty reduces at higher income levels where it becomes more affordable — just like how spending habits that are reckless at 20 may be perfectly reasonable at 40.

**Transport**

- Public Transport: +10 to +30 KP. Cost-effective and practical for most career stages.
- Cycling: 0 to -100 KP. Saves money early on, but as your career grows, the time it costs you becomes increasingly expensive.
- Car: -10 to -80 KP. High maintenance costs. The penalty scales down as your income rises and a car genuinely makes sense for your lifestyle.

The pattern across all three is intentional: there is no universally correct choice. What is smart at one income level can be irresponsible at another. That is exactly how real financial decisions work.

### Other KP Effects

