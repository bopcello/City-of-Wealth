## The Disaster System — Why Insurance Exists

Every 15 to 19 days, a disaster strikes. You cannot prevent it. You can only be prepared for it.

This is the single most important real-world lesson in the game. Unexpected events — floods, economic crashes, health crises — happen to everyone. The people who survive them are the ones who planned ahead.

### Asset Disasters

These destroy up to 50% of a specific asset type.

| Disaster | Asset Destroyed |
|:---:|:---:|
| Flood | Land |
| Fire | Properties and Vehicles |
| Earthquake | Office Equipment and Machinery |

**Without insurance:** you lose those assets permanently and their value entirely.

**With insurance:** you receive 80% of the original cost of every destroyed asset as a payout. This is exactly how home or vehicle insurance works in real life — you pay a small regular premium so that a single disaster does not wipe out years of savings.

### Passive Income Disasters

These do not destroy assets directly but cripple your passive income for 20 cycles. Each also carries a 20% chance to permanently destroy one of your invested units, killing that income source entirely until you reinvest.

| Disaster | Investment Affected | Income Reduction |
|:---:|:---:|:---:|
| Drought | Farms | -75% |
| Landslide | Distribution Centers | -80% |
| Economy Crash | Factories | -60% |
| Mass Emigration | Apartments | -80% |
| Pandemic | IT Service Centers | -90% |

The diversity lesson here is embedded in the design. If all your passive income comes from one source, a single disaster can devastate you. Spreading investments across multiple types reduces that risk — the same principle behind not putting all your savings into one stock.

### Insurance

Insurance costs 5 Gems per cycle. It rewards you with +20 KP per cycle, a one-time +100 KP bonus, and crucially, an 80% payout on any assets lost in a disaster. Given how frequently disasters strike and how much assets cost to rebuild, the 5 Gem premium is almost always worth paying. The game is teaching you to think in terms of expected value, not just immediate cost.

