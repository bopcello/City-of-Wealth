**Unnecessary Assets:** Owning more assets than your current career level requires incurs a penalty of -50 to -100 KP per cycle. A Student hoarding 50 properties is not being smart — they are overextended with assets they cannot manage or use. The required asset counts are tied to the buildings your level unlocks (see the Assets and Buildings section in the next page).

**Debt:** -200 KP per cycle while in debt.

**Overtime:** Working overtime costs 50 KP per cycle. If you keep it up beyond 10 consecutive days, the penalty escalates to 1,000 KP. Burnout is real, and grinding non-stop is not a financial strategy — it is a liability.

